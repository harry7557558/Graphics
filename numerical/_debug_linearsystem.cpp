const double rand_std = 10.0;  // standard derivation
void randomSymmetricMatrix(int N, double *M) {
	for (int i = 0; i < N; i++) {
		for (int j = 0; j < N; j++) {
			M[i*N + j] = randf_n(rand_std);
		}
	}
	for (int i = 0; i < N; i++) {
		for (int j = 0; j <= i; j++) {
			M[i*N + j] = (M[j*N + i] += M[i*N + j]);
		}
	}
}
void randomVector(int N, double *A) {
	for (int i = 0; i < N; i++) {
		A[i] = 2.0*randf_n(rand_std);
	}
}



